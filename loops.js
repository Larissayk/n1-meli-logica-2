// Loop com while

let contador = 0;
while(contador < 6) {
    console.log('Olá');
    contador++; //contador = contador + 1
}

// Loop com Do...While

do{
    console.log('Olá');
    contador++;
}while(contador <6);


//Loop com For

for(let contador=0; contador<6; contador++){
    console.log('olá');
}